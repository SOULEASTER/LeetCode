class Solution {
    public boolean validUtf8(int[] data) {
        if (data.length == 1) {
            if ((data[0] & 0x80) == 0x00) {
                return true;
            } else {
                return false;
            }
        } else {
            int first = data[0];
            int length = 0;
            while ((first & 0x80) == 0x80) {
                length++;
                first = first << 1;
            }
            if (length > 4) {
                return false;
            }
            if (data.length < length - 1) {
                return false;
            }
            for (int i = 1; i <= length - 1; i++) {
                if (!((data[i] & 0xc0) == 0x80)) {
                    return false;
                }
            }
            return validUtf8(Arrays.copyOfRange(data, length - 1, data.length));
        }
    }
}
